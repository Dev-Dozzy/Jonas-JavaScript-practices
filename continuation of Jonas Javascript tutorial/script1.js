//Loops and Iteration

//For loop
for (var i = 1; i < 10; i += 2) {
  console.log(i);
}
//Example
var john = ["John", "Smith", 1967, "Designer", "False"];
for (i = 0; i < john.length; i++) {
  console.log(john[i]);
}

//While loop
var john = ["John", "Smith", 1967, "Designer", "False"];
var i = 0;
while (i < john.length);
{
  console.log(john[i]);
  i++;
}
